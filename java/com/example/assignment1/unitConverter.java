package com.example.assignment1;

public class unitConverter {

    public static double convertMetersToInches(double value){
        return value*39.3701;
    }

    public static double convertMetersToYards(double value){
        return value*1.09361;
    }

    public static double convertInchesToMeters(double value){
        return value*0.0254;
    }

    public static double convertInchesToYards(double value){
        return value*0.027778;
    }

    public static double convertYardsToMeters(double value){
        return value*0.9144;
    }

    public static double convertYardsToInches(double value){
        return value*36;
    }
}
